class User:
	def __init__(self, name):
		self.name = name

	def set_id(self, ids):
		self.id = ids

	def set_social_id(self, ids):
		self.social_id = ids

	def set_name(self, name):
		self.name = name

	def set_username(self, username):
		self.username = username

	def set_salt(self, salt):
		self.salt = salt

	def set_password(self, password):
		self.password = password